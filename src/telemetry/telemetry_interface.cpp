// Secure Buffer Size (Page-Aligned)
constexpr size_t SECURE_BUFFER_SIZE = 4096;
constexpr size_t HASH_OUTPUT_SIZE = 32; // SHA3-256

// Telemetry Stream Types
enum class StreamType : uint8_t {
    EEG_RAW = 0x01,
    EMG_RAW = 0x02,
    XR_VISUAL = 0x03,
    XR_AUDITORY = 0x04,
    SYSTEM_METRIC = 0x05,
    RESERVED = 0xFF
};

// Consent Status Flags
enum class ConsentStatus : uint8_t {
    NONE = 0x00,
    SESSION_ACTIVE = 0x01,
    EXPLICIT_GRANTED = 0x02,
    REVOKED = 0x03
};

