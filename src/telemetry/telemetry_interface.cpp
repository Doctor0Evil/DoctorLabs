/**
 * @class SecureBuffer
 * @brief Manages non-pageable memory for sensitive telemetry data.
 *        Prevents swap-file leakage of neural information.
 */
class SecureBuffer {
private:
    void* m_data;
    size_t m_size;
    bool m_locked;

    // Platform-specific lock/unlock (placeholder for mlock/mprotect)
    bool lock_memory() {
        // In production: call mlock(m_data, m_size)
        // Return false if privileged operation fails
        m_locked = true; 
        return true;
    }

    void unlock_memory() {
        // In production: call munlock(m_data, m_size)
        m_locked = false;
    }

public:
    explicit SecureBuffer(size_t size) : m_size(size), m_locked(false) {
        // Allocate aligned memory
        m_data = aligned_alloc(64, size);
        if (!m_data) throw std::bad_alloc();
        
        // Zero initialize
        std::memset(m_data, 0, size);
        
        // Lock to prevent swapping
        if (!lock_memory()) {
            free(m_data);
            throw std::runtime_error("Failed to lock secure memory");
        }
    }

    ~SecureBuffer() {
        if (m_locked) unlock_memory();
        if (m_data) {
            // Secure wipe before free
            volatile uint8_t* p = static_cast<volatile uint8_t*>(m_data);
            for (size_t i = 0; i < m_size; ++i) p[i] = 0;
            free(m_data);
        }
    }

    // Non-copyable
    SecureBuffer(const SecureBuffer&) = delete;
    SecureBuffer& operator=(const SecureBuffer&) = delete;

    // Moveable
    SecureBuffer(SecureBuffer&& other) noexcept 
        : m_data(other.m_data), m_size(other.m_size), m_locked(other.m_locked) {
        other.m_data = nullptr;
        other.m_locked = false;
    }

    [[nodiscard]] void* data() { return m_data; }
    [[nodiscard]] const void* data() const { return m_data; }
    [[nodiscard]] size_t size() const { return m_size; }
};

