// Global Singleton for Stream Manager (Managed via Rust)
static StreamManager* g_stream_manager = nullptr;

extern "C" {

    /**
     * @brief Initializes the C++ telemetry subsystem.
     * @return 0 on success, -1 on failure
     */
    int telemetry_init() {
        if (g_stream_manager != nullptr) {
            return -1; // Already initialized
        }
        try {
            g_stream_manager = new StreamManager();
            return 0;
        } catch (...) {
            return -1;
        }
    }

    /**
     * @brief Starts a telemetry session with user consent.
     * @param token_ptr Pointer to consent token bytes
     * @param token_len Length of token
     * @return 1 if granted, 0 if denied
     */
    int telemetry_start_session(const uint8_t* token_ptr, size_t token_len) {
        if (!g_stream_manager || !token_ptr) return 0;
        std::span<const uint8_t> token(token_ptr, token_len);
        return g_stream_manager->initialize_stream(token) ? 1 : 0;
    }

    /**
     * @brief Pushes sensor data to the sanitizer.
     * @param stream_type Enum value for sensor type
     * @param data_ptr Pointer to raw sensor data
     * @param data_len Length of data
     * @param out_hash Buffer to receive sanitized hash (32 bytes)
     * @return 1 if successful, 0 if consent missing or error
     */
    int telemetry_push_data(uint8_t stream_type, const uint8_t* data_ptr, 
                            size_t data_len, uint8_t* out_hash) {
        if (!g_stream_manager || !data_ptr || !out_hash) return 0;
        
        std::span<const uint8_t> data(data_ptr, data_len);
        auto result = g_stream_manager->ingest_data(static_cast<StreamType>(stream_type), data);
        
        if (result.has_value()) {
            std::memcpy(out_hash, result->data(), HASH_OUTPUT_SIZE);
            return 1;
        }
        return 0;
    }

    /**
     * @brief Immediately revokes access and wipes state.
     *        Called by Rust Guardian on threat detection.
     */
    void telemetry_revoke() {
        if (g_stream_manager) {
            g_stream_manager->revoke_access();
        }
    }

    /**
     * @brief Cleans up the telemetry subsystem.
     */
    void telemetry_shutdown() {
        if (g_stream_manager) {
            g_stream_manager->revoke_access();
            delete g_stream_manager;
            g_stream_manager = nullptr;
        }
    }
}

