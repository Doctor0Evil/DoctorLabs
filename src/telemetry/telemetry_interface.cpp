/**
 * @class StreamManager
 * @brief Aggregates and sanitizes multiple sensor streams.
 *        Enforces monotone capability rules on data access.
 */
class StreamManager {
private:
    std::unique_ptr<SecureBuffer> m_buffer;
    std::atomic<ConsentStatus> m_consent;
    std::mutex m_stream_mutex;
    std::array<uint8_t, HASH_OUTPUT_SIZE> m_session_token;

public:
    explicit StreamManager() : m_consent(ConsentStatus::NONE) {
        m_buffer = std::make_unique<SecureBuffer>(SECURE_BUFFER_SIZE);
        std::memset(m_session_token.data(), 0, HASH_OUTPUT_SIZE);
    }

    /**
     * @brief Initializes stream with user consent.
     * @param token_raw Raw consent token from user agent
     * @return true if initialization successful
     */
    bool initialize_stream(const std::span<const uint8_t> token_raw) {
        std::lock_guard<std::mutex> lock(m_stream_mutex);
        
        if (m_consent.load() == ConsentStatus::SESSION_ACTIVE) {
            return false; // Already active
        }

        // Hash token immediately
        auto token_hash = TelemetrySanitizer::hash_span(token_raw);
        
        // Validate against governance ledger
        if (!TelemetrySanitizer::validate_consent(token_hash)) {
            return false; // Consent invalid
        }

        m_session_token = token_hash;
        m_consent.store(ConsentStatus::SESSION_ACTIVE);
        return true;
    }

    /**
     * @brief Ingests raw sensor data into secure buffer.
     * @param stream_type Type of sensor data
     * @param data Raw sensor bytes
     * @return Sanitized hash for logging (Raw data stays enclave)
     */
    std::optional<std::array<uint8_t, HASH_OUTPUT_SIZE>> 
    ingest_data(StreamType stream_type, const std::span<const uint8_t> data) {
        if (m_consent.load() != ConsentStatus::SESSION_ACTIVE) {
            return std::nullopt; // Consent missing
        }

        // Security Check: Prevent Buffer Overflow
        if (data.size() > SECURE_BUFFER_SIZE) {
            return std::nullopt;
        }

        std::lock_guard<std::mutex> lock(m_stream_mutex);

        // Copy to secure enclave
        std::memcpy(m_buffer->data(), data.data(), data.size());

        // Sanitize: Hash the data for external logging
        auto sanitized_hash = TelemetrySanitizer::hash_span(data);

        // Secure Wipe Buffer after processing
        volatile uint8_t* ptr = static_cast<volatile uint8_t*>(m_buffer->data());
        for (size_t i = 0; i < data.size(); ++i) ptr[i] = 0;

        return sanitized_hash;
    }

    /**
     * @brief Revokes access and wipes session state.
     */
    void revoke_access() {
        std::lock_guard<std::mutex> lock(m_stream_mutex);
        m_consent.store(ConsentStatus::REVOKED);
        std::memset(m_session_token.data(), 0, HASH_OUTPUT_SIZE);
        
        // Wipe buffer
        if (m_buffer) {
            volatile uint8_t* ptr = static_cast<volatile uint8_t*>(m_buffer->data());
            for (size_t i = 0; i < m_buffer->size(); ++i) ptr[i] = 0;
        }
    }

    [[nodiscard]] ConsentStatus get_consent_status() const {
        return m_consent.load();
    }
};

