use std::process::Command;
use std::time::{SystemTime, UNIX_EPOCH};

// ==============================================================================
// MAIN BUILD FUNCTION
// ==============================================================================

fn main() -> Result<(), Box<dyn std::error::Error>> {
    println!("cargo:rerun-if-changed=build.rs");
    println!("cargo:rerun-if-changed=Cargo.toml");
    println!("cargo:rerun-if-changed=.git/HEAD");

    // Create output directory
    let out_dir = env::var("OUT_DIR")?;
    let dest_path = Path::new(&out_dir).join("build_info.rs");
    let mut file = BufWriter::new(File::create(&dest_path)?);

    println!("cargo:warning=Doctor-Labs SuperFilter Build Script Starting...");

    // Generate all build information
    generate_crate_info(&mut file)?;
    generate_git_info(&mut file)?;
    generate_build_info(&mut file)?;
    generate_compliance_info(&mut file)?;
    generate_feature_info(&mut file)?;
    generate_platform_info(&mut file)?;

    // Flush and sync
    file.flush()?;
    file.sync_all()?;

    println!("cargo:warning=Doctor-Labs SuperFilter Build Script Complete.");
    println!("cargo:warning=Build info written to: {}", dest_path.display());

    Ok(())
}

