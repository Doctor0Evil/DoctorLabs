//! Build script for doctor_labs_superfilter crate.
//!
//! This script generates compile-time constants for:
//! - Crate version and build information
//! - Git commit hash and branch information
//! - Build timestamp and environment details
//! - Compliance framework markers
//! - Feature flag status
//!
//! Generated code is output to OUT_DIR/build_info.rs and included in lib.rs.

#![deny(clippy::all)]
#![warn(missing_docs)]

use std::env;
