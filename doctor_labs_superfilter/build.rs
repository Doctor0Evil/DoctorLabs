/// Get short Git commit hash.
fn get_git_hash_short() -> Result<String, Box<dyn std::error::Error>> {
    let output = Command::new("git")
        .args(["rev-parse", "--short", "HEAD"])
        .output()?;

    if output.status.success() {
        Ok(String::from_utf8_lossy(&output.stdout).trim().to_string())
    } else {
        Err("Failed to get git hash".into())
    }
}

/// Get full Git commit hash.
fn get_git_hash_full() -> Result<String, Box<dyn std::error::Error>> {
    let output = Command::new("git")
        .args(["rev-parse", "HEAD"])
        .output()?;

    if output.status.success() {
        Ok(String::from_utf8_lossy(&output.stdout).trim().to_string())
    } else {
        Err("Failed to get git hash full".into())
    }
}

/// Get Git branch name.
fn get_git_branch() -> Result<String, Box<dyn std::error::Error>> {
    let output = Command::new("git")
        .args(["rev-parse", "--abbrev-ref", "HEAD"])
        .output()?;

    if output.status.success() {
        Ok(String::from_utf8_lossy(&output.stdout).trim().to_string())
    } else {
        Err("Failed to get git branch".into())
    }
}

/// Check if Git working tree is dirty.
fn is_git_dirty() -> Result<bool, Box<dyn std::error::Error>> {
    let output = Command::new("git")
        .args(["status", "--porcelain"])
        .output()?;

    if output.status.success() {
        Ok(!String::from_utf8_lossy(&output.stdout).trim().is_empty())
    } else {
        Ok(false)
    }
}

/// Get Git commit timestamp.
fn get_git_commit_timestamp() -> Result<String, Box<dyn std::error::Error>> {
    let output = Command::new("git")
        .args(["log", "-1", "--format=%ct"])
        .output()?;

    if output.status.success() {
        Ok(String::from_utf8_lossy(&output.stdout).trim().to_string())
    } else {
        Err("Failed to get git timestamp".into())
    }
}

/// Get Git tags at current commit.
fn get_git_tags() -> Result<String, Box<dyn std::error::Error>> {
    let output = Command::new("git")
        .args(["tag", "--points-at", "HEAD"])
        .output()?;

    if output.status.success() {
        let tags = String::from_utf8_lossy(&output.stdout).trim().to_string();
        if tags.is_empty() {
            Ok("none".to_string())
        } else {
            Ok(tags.replace('\n', ", "))
        }
    } else {
        Ok("none".to_string())
    }
}

