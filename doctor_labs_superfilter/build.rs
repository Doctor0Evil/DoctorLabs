/// Generate build timestamp and environment information.
fn generate_build_info(file: &mut BufWriter<File>) -> std::io::Result<()> {
    writeln!(file, "// =============================================================================")?;
    writeln!(file, "// BUILD INFORMATION (Auto-generated by build.rs)")?;
    writeln!(file, "// =============================================================================")?;
    writeln!(file)?;

    // Build timestamp (UNIX epoch milliseconds)
    let build_timestamp_ms = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .as_millis();
    writeln!(file, "/// Build timestamp (UNIX epoch milliseconds)")?;
    writeln!(file, "pub const BUILD_TIMESTAMP_MS: u128 = {};", build_timestamp_ms)?;
    writeln!(file)?;

    // Build timestamp (ISO 8601)
    let build_timestamp_iso = chrono::Utc::now().format("%Y-%m-%dT%H:%M:%SZ").to_string();
    writeln!(file, "/// Build timestamp (ISO 8601)")?;
    writeln!(file, "pub const BUILD_TIMESTAMP_ISO: &str = \"{}\";", build_timestamp_iso)?;
    writeln!(file)?;

    // Build profile (debug/release)
    let profile = env::var("PROFILE").unwrap_or_else(|_| "unknown".to_string());
    writeln!(file, "/// Build profile")?;
    writeln!(file, "pub const BUILD_PROFILE: &str = \"{}\";", profile)?;
    writeln!(file)?;

    // Optimization level
    let opt_level = env::var("OPT_LEVEL").unwrap_or_else(|_| "0".to_string());
    writeln!(file, "/// Optimization level")?;
    writeln!(file, "pub const BUILD_OPT_LEVEL: &str = \"{}\";", opt_level)?;
    writeln!(file)?;

    // Debug assertions
    let debug_assertions = env::var("DEBUG").unwrap_or_else(|_| "true".to_string()) == "true";
    writeln!(file, "/// Debug assertions enabled")?;
    writeln!(file, "pub const BUILD_DEBUG_ASSERTIONS: bool = {};", debug_assertions)?;
    writeln!(file)?;

    // Target triple
    let target = env::var("TARGET").unwrap_or_else(|_| "unknown".to_string());
    writeln!(file, "/// Build target triple")?;
    writeln!(file, "pub const BUILD_TARGET: &str = \"{}\";", target)?;
    writeln!(file)?;

    // Host triple
    let host = env::var("HOST").unwrap_or_else(|_| "unknown".to_string());
    writeln!(file, "/// Build host triple")?;
    writeln!(file, "pub const BUILD_HOST: &str = \"{}\";", host)?;
    writeln!(file)?;

    // Number of CPUs
    let num_cpus = num_cpus::get();
    writeln!(file, "/// Number of CPUs used for build")?;
    writeln!(file, "pub const BUILD_NUM_CPUS: usize = {};", num_cpus)?;
    writeln!(file)?;

    Ok(())
}

